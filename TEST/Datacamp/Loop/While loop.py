# While loop = repeated if statement
''''
while condition:
    expression
'''''

error = 50.0
while error >1:
    error = error/4
    print(error)

x = 1
while x < 4 :
    print(x)
    x = x + 1

# Initialize offset
offcet = 8

# Code the while loop
while offcet != 0:
    print("correcting...")
    offcet = offcet - 1
    print(offcet)

# Initialize offset
offset = -6

# Code the while loop
while offset != 0 :
    print("correcting...")
    if offset > 0:
      offset = offset- 1
    else :
      offset = offset + 1
    print(offset)